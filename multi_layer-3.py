class BRPTracker:
    """Tracks bias accumulation across CNN layers [cite: 131]"""
    def __init__(self):
        self.activations = {}
        self.hooks = []

    def hook_fn(self, name):
        def hook(model, input, output):
            self.activations[name] = output.detach()
        return hook

    def calculate_brp_score(self, labels):
        """Formula: |Mean(F_A) - Mean(F_B)| / Std(F_all) [cite: 134]"""
        scores = {}
        group_a_idx = (labels == 1).nonzero(as_tuple=True)[0] # Male
        group_b_idx = (labels == 0).nonzero(as_tuple=True)[0] # Female

        for layer, feat in self.activations.items():
            if len(group_a_idx) > 1 and len(group_b_idx) > 1:
                feat_flat = feat.view(feat.size(0), -1)
                mean_a = feat_flat[group_a_idx].mean()
                mean_b = feat_flat[group_b_idx].mean()
                std_total = feat_flat.std()
                # Store absolute score as per paper [cite: 134]
                scores[layer] = (torch.abs(mean_a - mean_b) / (std_total + 1e-7)).item()
        return scores

