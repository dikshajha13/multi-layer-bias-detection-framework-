import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

def plot_bias_flow_analysis(layer_bias_scores):
    """
    Inputs: Dictionary or list of bias scores from Layer 1 to 4.
    Example: [0.0075, 0.0152, 0.0248, 0.0345]
    """
    layers = ['Layer 1', 'Layer 2', 'Layer 3', 'Layer 4']
    scores = np.array(layer_bias_scores)
    total_bias = scores[-1] # The final bias at Layer 4 is the 'Total'

    # 1. Calculate Incremental Transfer (What is "moved" to this layer)
    transfer = [scores[0]] # First layer is the starting point
    for i in range(1, len(scores)):
        transfer.append(scores[i] - scores[i-1])

    # 2. Calculate % of Total Bias at each step
    percent_of_total = (scores / total_bias) * 100

    # Create Dataframe for analysis
    df = pd.DataFrame({
        'Layer': layers,
        'Cumulative Bias': scores,
        'Transfer (Added Bias)': transfer,
        'Percent of Final Bias (%)': percent_of_total
    })

    # --- VISUALIZATION ---
    fig, ax1 = plt.subplots(figsize=(12, 7))
    sns.set_style("white")

    # Bar chart for Transfer (The 'new' bias added at each stage)
    bars = ax1.bar(df['Layer'], df['Transfer (Added Bias)'], color='#3498db', alpha=0.7, label='Layer Transfer (New Bias)')

    # Line chart for Cumulative Propagation (The 'Bias Left' at each stage)
    ax2 = ax1.twinx()
    ax2.plot(df['Layer'], df['Cumulative Bias'], color='#e74c3c', marker='D', markersize=8, linewidth=3, label='Cumulative Bias Intensity')

    # Formatting
    ax1.set_ylabel('Incremental Transfer Score', fontsize=12, fontweight='bold', color='#3498db')
    ax2.set_ylabel('Total Bias Intensity', fontsize=12, fontweight='bold', color='#e74c3c')
    plt.title("Figure 8: Bias Transfer Flow (Layer-to-Layer Accumulation)", fontsize=14, fontweight='bold')

    # Add Value Annotations
    for i, p in enumerate(bars):
        ax1.annotate(f"+{df['Transfer (Added Bias)'][i]:.4f}\n({df['Percent of Final Bias (%)'][i]:.1f}%)",
                    (p.get_x() + p.get_width() / 2., p.get_height() / 2),
                    ha='center', va='center', color='white', fontweight='bold', fontsize=10)

    print("\n--- Detailed Bias Transfer Analysis ---")
    print(df.to_string(index=False))

    plt.tight_layout()
    plt.show()

