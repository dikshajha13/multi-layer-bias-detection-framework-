def compute_ddft_weights(dataset_a_dist, dataset_b_dist):
    """
    Implements Section III-G: Compares CelebA (A) with UTKFace (B)
    to generate fairness guidance weights.
    """
    # Majority vs Minority ratios
    # Example: CelebA is 58/42 while UTKFace is 50/50
    ratio_a = dataset_a_dist['Male'] / dataset_a_dist['Female']
    ratio_b = dataset_b_dist['Male'] / dataset_b_dist['Female']

    # Guidance weight to pull CelebA distribution toward UTKFace 'Fair' distribution
    guidance_weight = ratio_b / ratio_a
    return guidance_weight

# 2. REPRESENTATION ADJUSTMENT (Phase 5)
def ddf_loss_function(outputs, targets, sensitive_attr, guidance_weight):
    """
    Custom Loss that applies DDFT guidance to penalize biased predictions
    during the final representation adjustment phase.
    """
    standard_loss = F.binary_cross_entropy(outputs, targets, reduction='none')

    # Apply higher penalty to majority group errors to force fairness convergence
    # Based on Fairness Guidance Vector (FGV)
    weights = torch.ones_like(targets)
    weights[sensitive_attr == 1] *= guidance_weight

    weighted_loss = (standard_loss * weights).mean()
    return weighted_loss

