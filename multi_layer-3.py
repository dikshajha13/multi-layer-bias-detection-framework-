def evaluate_full_framework(model, loader, tracker):
    model.eval()
    all_preds, all_labels = [], []
    layer_bias_history = []

    with torch.no_grad():
        for images, labels in loader:
            images, labels = images.to(device), labels.to(device)
            outputs = torch.sigmoid(model(images)).squeeze()

            # Record BRP for this batch [cite: 133]
            batch_scores = tracker.calculate_brp_score(labels)
            if batch_scores: layer_bias_history.append(batch_scores)

            all_preds.extend((outputs > 0.5).cpu().numpy())
            all_labels.extend(labels.cpu().numpy())

    # Calculate Aggregate Fairness Metrics [cite: 148]
    results = pd.DataFrame({'true': all_labels, 'pred': all_preds})

    # Demographic Parity: P(Y_hat=1 | Male) vs P(Y_hat=1 | Female)
    dp_male = results[results['true'] == 1]['pred'].mean()
    dp_female = results[results['true'] == 0]['pred'].mean()
    dp_gap = abs(dp_male - dp_female)

    # Equal Opportunity: TPR_male vs TPR_female
    def get_tpr(df):
        tp = ((df['pred'] == 1) & (df['true'] == 1)).sum()
        p = (df['true'] == 1).sum()
        return tp / p if p > 0 else 0

    eo_male = get_tpr(results[results['true'] == 1])
    eo_female = get_tpr(results[results['true'] == 0])
    eo_gap = abs(eo_male - eo_female)

    # Average BRP Scores
    avg_brp = pd.DataFrame(layer_bias_history).mean().to_dict()

    return dp_gap, eo_gap, avg_brp

